    public class UserController : Controller
    {
        public UserController(IUserService todoItems)
        {
            TodoItems = todoItems;
        }
        public IUserService TodoItems { get; set; }

        [HttpGet("{id}", Name = "GetUserTokenById")]
        public IActionResult GetUserToken(string id)
        {
            var item = TodoItems.UserTokenGetById(id);
            if (item == null)
            {
                return NotFound();
            }
            return new ObjectResult(item);
        }
